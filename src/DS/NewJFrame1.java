    static ArrayList<Contact> temparr;
    public static void refreshcontact() throws IOException{
        java.io.File file = new java.io.File(g.localcontactdir);
        String [] data = file.list();
        
        tabelcontact = new DefaultComboBoxModel();
        
        temparr = new ArrayList<>();
       
        
        for (String string : data) {
            temparr.add(new Contact(g.fi.ReadFileFirstLine(g.localcontactdir+string),g.fi.getPathName(string)));
        }
        Collections.sort(temparr, new Comparator<Contact>() {
            @Override
            public int compare(Contact o1, Contact o2) {
                return o1.getName().compareTo(o2.getName());
            }
        });
        
        for(Contact c : temparr){
            tabelcontact.addElement(c);
        }
        
        jComboBox1.setModel(tabelcontact);
        jComboBox1.setSelectedIndex(0);
    }

