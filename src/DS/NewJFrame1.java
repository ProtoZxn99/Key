    private void SetKeyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SetKeyActionPerformed
        // TODO add your handling code here:
        JOptionPane.showMessageDialog(this, g.OpenKeyWarning);
        String path = g.fi.GetFilePath(this, g.localkeydir);
        String tmp = g.fi.getPathName(path);
        if(path.length()>0 && g.fi.findFile(tmp+".key")&& g.fi.findFile(tmp+".pub")){
            
            try {
                String name = g.fi.getPathName(path);
                PrivateKey pri = g.sr.loadPrivate(name+".key");
                PublicKey pub = g.sr.loadPublic(name+".pub");
                
                g.sr = new SecurityRSA(pri, pub);
                
                g.sr.savePrivate(g.localkeydir, g.mac);
                g.sr.savePublic(g.localkeydir, g.mac);
                
            } catch (IOException ex) {
                Logger.getLogger(NewJFrame1.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NoSuchAlgorithmException ex) {
                Logger.getLogger(NewJFrame1.class.getName()).log(Level.SEVERE, null, ex);
            } catch (InvalidKeySpecException ex) {
                Logger.getLogger(NewJFrame1.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "error");
        }
    }//GEN-LAST:event_SetKeyActionPerformed

